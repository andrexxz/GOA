# 1) Function to double an integer
def double_integer(num):
    return num * 2

# 2) Function to greet a person
def greet(name):
    return f"Hello, {name}!"

# 3) Function to check if a number is even
def is_even(num):
    return num % 2 == 0

# 4) Simple function with a default value
def favorite_food(food="pizza"):
    return f"My favorite food is {food}."

# 5) Function to return the first three letters of a name
def nickname(name):
    return name[:3]
