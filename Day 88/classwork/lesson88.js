// 1. while loop დახმარებით, 50-ჯერ 'GOA' გამოშვება
let count = 0;
while (count < 50) {
    console.log("GOA");
    count++;
}

// 2. while ციკლის საშვალებით რიცხვების გამოშვება 20-დან 0-ით ჩათვლით
let i = 20;
while (i >= 0) {
    console.log(i);
    i--;
}

// 3. while ციკლის საშვალებით 1-დან 20-მდე მხოლოდ ლუწი რიცხვების გამოშვება
let j = 2;
while (j <= 20) {
    console.log(j);
    j += 2;
}

// 4. while ციკლის საშვალებით 1-დან 100-მდე ყველა 5-ის ჯერადი რიცხვის გამოშვება
let k = 5;
while (k <= 100) {
    console.log(k);
    k += 5;
}
