# სიის შექმნა
my_list = ["Python", "is", "fun", "and", "powerful"]

# თვისება 1: ელემენტის დამატება
my_list.append("language")
print("სიაში დამატების შემდეგ:", my_list)

# თვისება 2: ელემენტის წაშლა
my_list.remove("fun")
print("სიიდან წაშლის შემდეგ:", my_list)

# თვისება 3: ელემენტების რაოდენობის დათვლა
length = len(my_list)
print("სიის ელემენტების რაოდენობა:", length)
